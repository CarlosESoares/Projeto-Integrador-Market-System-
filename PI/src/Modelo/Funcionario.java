package Modelo;

public class Funcionario {
	
public String NomeFuncionario;
public String SobrenomeFuncionario;
public int IdFuncionario;
public String SenhaFuncionario;
public float TelefoneFuncionario;
public String AreaTrabalho;
public String Endereco;
public double Salario;
public String tipoFucionario;



public String getTipoFucionario() {
	return tipoFucionario;
}
public void setTipoFucionario(String tipoFucionario) {
	this.tipoFucionario = tipoFucionario;
}
public String getNomeFuncionario() {
	return NomeFuncionario;
}
public void setNomeFuncionario(String nomeFuncionario) {
	NomeFuncionario = nomeFuncionario;
}
public String getSobrenomeFuncionario() {
	return SobrenomeFuncionario;
}
public void setSobrenomeFuncionario(String sobrenomeFuncionario) {
	SobrenomeFuncionario = sobrenomeFuncionario;
}
public int getIdFuncionario() {
	return IdFuncionario;
}
public void setIdFuncionario(int idFuncionario) {
	IdFuncionario = idFuncionario;
}
public String getSenhaFuncionario() {
	return SenhaFuncionario;
}
public void setSenhaFuncionario(String senhaFuncionario) {
	SenhaFuncionario = senhaFuncionario;
}
public float getTelefoneFuncionario() {
	return TelefoneFuncionario;
}
public void setTelefoneFuncionario(float telefoneFuncionario) {
	TelefoneFuncionario = telefoneFuncionario;
}
public String getAreaTrabalho() {
	return AreaTrabalho;
}
public void setAreaTrabalho(String areaTrabalho) {
	AreaTrabalho = areaTrabalho;
}
public String getEndereco() {
	return Endereco;
}
public void setEndereco(String endereco) {
	Endereco = endereco;
}
public double getSalario() {
	return Salario;
}
public void setSalario(double salario) {
	Salario = salario;
}

}
